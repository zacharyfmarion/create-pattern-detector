def artifact_slice(summary: dict[str, Any], variant: str) -> dict[str, Any]:
    keys = [
        f"{issue}/{variant}"
        for issue in ["text_false_positive", "watermark_false_positive", "guide_grid_false_positive"]
    ]
    slices = [summary["by_issue_variant"][key] for key in keys if key in summary["by_issue_variant"]]
    if not slices:
        return {}
    return {
        "samples": sum(int(item.get("samples", 0)) for item in slices),
        "edge_f1": mean_non_null(item.get("edge_f1") for item in slices),
        "border_f1": mean_non_null(item.get("border_f1") for item in slices),
        "boundary_contact_f1": mean_non_null(item.get("boundary_contact_f1") for item in slices),
        "carrier_recall": mean_non_null(item.get("carrier_recall") for item in slices),
        "artifact_false_positive_rate": mean_non_null(
            item.get("artifact_false_positive_rate") for item in slices
        ),
    }


