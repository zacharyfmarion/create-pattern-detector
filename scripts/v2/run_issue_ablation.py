def build_model_variant(
    prediction: ModelPrediction,
    *,
    builder: PlanarGraphBuilder,
    assignment_config: EdgeAssignmentConfig,
    repair_config: RepairConfig,
    report_config: QualityReportConfig,
    infer_assignments: bool,
    extra_repair_code: str | None = None,
) -> VariantResult:
    evidence = VectorizerEvidence(
        line_prob=prediction.line_prob,
        angle=prediction.angle,
        junction_heatmap=prediction.junction_heatmap,
        assignment_labels=None,
    )
    graph_result = builder.build(evidence)
    attributed = attribute_graph_from_logits(
        graph_result,
        prediction.assignment_logits,
        line_prob=prediction.line_prob,
        config=assignment_config,
    )
    repair = conservative_repair(
        attributed,
        line_prob=prediction.line_prob,
        config=repair_config,
        infer_assignments=infer_assignments,
    )
    repair_codes = [action.code for action in repair.actions]
    if extra_repair_code:
        repair_codes.append(extra_repair_code)
    report = build_quality_report(repair.graph, repair_actions=repair.actions, config=report_config)
    return VariantResult(
        graph=repair.graph,
        status=report.status,
        warning_codes=[warning.code for warning in report.warnings],
        repair_codes=repair_codes,
    )


def build_graph_variant(
    graph: AttributedPlanarGraph,
    *,
    report_config: QualityReportConfig,
    extra_repair_code: str,
) -> VariantResult:
    action = RepairAction(code=extra_repair_code, message=f"Eval-only {extra_repair_code}.")
    report = build_quality_report(graph, repair_actions=[action], config=report_config)
    return VariantResult(
        graph=graph,
        status=report.status,
        warning_codes=[warning.code for warning in report.warnings],
        repair_codes=[extra_repair_code],
    )


def build_oracle_evidence_variant(
    target: TargetGraph,
    *,
    builder: PlanarGraphBuilder,
    repair_config: RepairConfig,
    report_config: QualityReportConfig,
) -> VariantResult:
    rendered = render_vectorizer_evidence_from_pixels(
        pixel_vertices=target.vertices,
        edges=target.edges,
        assignments=target.assignments,
        image_size=target.image_size,
        line_width=max(1, int(round(2 * target.image_size / 768))),
        junction_sigma=max(1.5, 2.0 * target.image_size / 768),
        junction_radius=max(4.0, 4.0 * target.image_size / 768),
    )
    graph_result = builder.build(rendered.evidence)
    attributed = AttributedPlanarGraph.from_planar_result(graph_result)
    repair = conservative_repair(
        attributed,
        line_prob=rendered.evidence.line_prob,
        config=repair_config,
    )
    report = build_quality_report(repair.graph, repair_actions=repair.actions, config=report_config)
    return VariantResult(
        graph=repair.graph,
        status=report.status,
        warning_codes=[warning.code for warning in report.warnings],
        repair_codes=["oracle_vector_evidence", *[action.code for action in repair.actions]],
    )


def evaluate_variant(
    variant_result: VariantResult,
    *,
    target: TargetGraph,
    example: dict[str, Any],
    variant: str,
    threshold: float,
    artifact_mask: np.ndarray | None,
) -> tuple[GraphMetrics, dict[str, Any]]:
    metrics_obj = evaluate_graph(
        variant_result.graph.to_planar_result(),
        gt_vertices=target.vertices,
        gt_edges=target.edges,
        gt_assignments=target.assignments,
        vertex_tolerance_px=max(3.0, 5.0 * target.image_size / 768),
    )
    metrics = metrics_obj.to_dict()
    extra = compute_v2_metrics(
        variant_result.graph.to_planar_result(),
        gt_vertices=target.vertices,
        gt_edges=target.edges,
        gt_assignments=target.assignments,
        image_size=target.image_size,
        issue=str(example["issue"]),
        artifact_mask=artifact_mask,
    )
    source_counts = Counter(variant_result.graph.assignment_source)
    row = {
        "id": example["id"],
        "issue": example["issue"],
        "variant": variant,
        "status": variant_result.status,
        "warnings": variant_result.warning_codes,
        "repairs": variant_result.repair_codes,
        "threshold": threshold,
        "observed_edges": int(source_counts.get("observed", 0)),
        "unknown_edges": int(source_counts.get("unknown", 0)),
        "inferred_edges": int(source_counts.get("inferred", 0)),
        "oracle_edges": int(source_counts.get("oracle", 0)),
        **metrics,
        **extra,
    }
    row["edge_f1"] = f1(float(row["edge_precision"]), float(row["edge_recall"]))
    return metrics_obj, row


def compute_v2_metrics(
    result: PlanarGraphResult,
    *,
    gt_vertices: np.ndarray,
    gt_edges: np.ndarray,
    gt_assignments: np.ndarray,
    image_size: int,
    issue: str,
    artifact_mask: np.ndarray | None,
) -> dict[str, Any]:
    contact = boundary_contact_metrics(
        pred_vertices=result.pixel_vertices,
        gt_vertices=gt_vertices,
        gt_edges=gt_edges,
        gt_assignments=gt_assignments,
        image_size=image_size,
    )
    carriers = carrier_metrics(
        pred_vertices=result.pixel_vertices,
        pred_edges=result.edges_vertices,
        pred_assignments=result.edges_assignment,
        gt_vertices=gt_vertices,
        gt_edges=gt_edges,
        gt_assignments=gt_assignments,
    )
    artifact = artifact_false_positive_rate(
        result,
        gt_vertices=gt_vertices,
        gt_edges=gt_edges,
        artifact_mask=artifact_mask,
        image_size=image_size,
        enabled=issue in ARTIFACT_ISSUES,
    )
    return {
        **contact,
        **carriers,
        **artifact,
        "dashed_carrier_recall": carriers["carrier_recall"]
        if issue == "dashed_line_support"
        else None,
    }


def boundary_contact_metrics(
    *,
    pred_vertices: np.ndarray,
    gt_vertices: np.ndarray,
    gt_edges: np.ndarray,
    gt_assignments: np.ndarray,
    image_size: int,
    frame_tolerance_px: float | None = None,
    match_tolerance_px: float | None = None,
) -> dict[str, float | int]:
    frame_tol = frame_tolerance_px or max(3.0, 4.0 * image_size / 768)
    match_tol = match_tolerance_px or max(4.0, 6.0 * image_size / 768)
    frame = infer_square_frame(gt_vertices, gt_edges, gt_assignments, image_size)
    pred_contacts = boundary_contacts(pred_vertices, frame, frame_tol)
    gt_contacts = boundary_contacts(gt_vertices, frame, frame_tol)
    matched = match_boundary_contacts(pred_contacts, gt_contacts, frame, match_tol)
    corner_recall = match_corners(pred_vertices, frame, match_tol)
    precision = ratio(matched, len(pred_contacts))
    recall = ratio(matched, len(gt_contacts))
    return {
        "boundary_contact_precision": precision,
        "boundary_contact_recall": recall,
        "boundary_contact_f1": f1(precision, recall),
        "boundary_contact_matched": matched,
        "boundary_contact_pred": len(pred_contacts),
        "boundary_contact_gt": len(gt_contacts),
        "corner_recall": corner_recall,
    }


def boundary_contacts(
    vertices: np.ndarray,
    frame: SquareFrame,
    tolerance_px: float,
) -> list[tuple[str, float]]:
    contacts: list[tuple[str, float]] = []
    corners = np.array(
        [
            [frame.x_min, frame.y_min],
            [frame.x_max, frame.y_min],
            [frame.x_max, frame.y_max],
            [frame.x_min, frame.y_max],
        ],
        dtype=np.float32,
    )
    width = max(frame.x_max - frame.x_min, 1.0)
    height = max(frame.y_max - frame.y_min, 1.0)
    for vertex in np.asarray(vertices, dtype=np.float32):
        if np.min(np.linalg.norm(corners - vertex[None, :], axis=1)) <= tolerance_px:
            continue
        x, y = float(vertex[0]), float(vertex[1])
        candidates: list[tuple[str, float, float]] = [
            ("top", abs(y - frame.y_min), (x - frame.x_min) / width),
            ("right", abs(frame.x_max - x), (y - frame.y_min) / height),
            ("bottom", abs(frame.y_max - y), (x - frame.x_min) / width),
            ("left", abs(x - frame.x_min), (y - frame.y_min) / height),
        ]
        side, distance, coord = min(candidates, key=lambda item: item[1])
        if distance <= tolerance_px:
            contacts.append((side, float(np.clip(coord, 0.0, 1.0))))
    return contacts


def match_boundary_contacts(
    pred_contacts: list[tuple[str, float]],
    gt_contacts: list[tuple[str, float]],
    frame: SquareFrame,
    tolerance_px: float,
) -> int:
    matched = 0
    for side in ("top", "right", "bottom", "left"):
        pred = [coord for item_side, coord in pred_contacts if item_side == side]
        gt = [coord for item_side, coord in gt_contacts if item_side == side]
        if not pred or not gt:
            continue
        side_length = frame.x_max - frame.x_min if side in {"top", "bottom"} else frame.y_max - frame.y_min
        distances = np.abs(np.asarray(gt)[:, None] - np.asarray(pred)[None, :]) * max(side_length, 1.0)
        rows, cols = linear_sum_assignment(distances)
        matched += int(sum(float(distances[row, col]) <= tolerance_px for row, col in zip(rows, cols)))
    return matched


def match_corners(vertices: np.ndarray, frame: SquareFrame, tolerance_px: float) -> float:
    corners = np.array(
        [
            [frame.x_min, frame.y_min],
            [frame.x_max, frame.y_min],
            [frame.x_max, frame.y_max],
            [frame.x_min, frame.y_max],
        ],
        dtype=np.float32,
    )
    if len(vertices) == 0:
        return 0.0
    distances = np.linalg.norm(corners[:, None, :] - vertices[None, :, :], axis=2)
    return float(np.mean(np.min(distances, axis=1) <= tolerance_px))


def infer_square_frame(
    gt_vertices: np.ndarray,
    gt_edges: np.ndarray,
    gt_assignments: np.ndarray,
    image_size: int,
) -> SquareFrame:
    border_indices = np.flatnonzero(np.asarray(gt_assignments) == 2)
    if len(border_indices) == 0:
        return SquareFrame(0.0, 0.0, float(image_size - 1), float(image_size - 1))
    border_vertex_indices = sorted(
        {int(vertex) for edge_idx in border_indices for vertex in gt_edges[int(edge_idx)]}
    )
    border_vertices = np.asarray(gt_vertices, dtype=np.float32)[border_vertex_indices]
    return SquareFrame(
        x_min=float(np.min(border_vertices[:, 0])),
        y_min=float(np.min(border_vertices[:, 1])),
        x_max=float(np.max(border_vertices[:, 0])),
        y_max=float(np.max(border_vertices[:, 1])),
    )


def carrier_metrics(
    *,
    pred_vertices: np.ndarray,
    pred_edges: np.ndarray,
    pred_assignments: np.ndarray,
    gt_vertices: np.ndarray,
    gt_edges: np.ndarray,
    gt_assignments: np.ndarray,
) -> dict[str, float | int]:
    gt_carriers = derive_carriers(gt_vertices, gt_edges, gt_assignments)
    pred_carriers = derive_carriers(pred_vertices, pred_edges, pred_assignments)
    matched_gt, matched_pred = match_carriers(gt_carriers, pred_carriers)
    precision = ratio(matched_pred, len(pred_carriers))
    recall = ratio(matched_gt, len(gt_carriers))
    return {
        "carrier_precision": precision,
        "carrier_recall": recall,
        "carrier_f1": f1(precision, recall),
        "carrier_gt": len(gt_carriers),
        "carrier_pred": len(pred_carriers),
        "carrier_matched_gt": matched_gt,
        "carrier_matched_pred": matched_pred,
    }


def derive_carriers(
    vertices: np.ndarray,
    edges: np.ndarray,
    assignments: np.ndarray,
    *,
    angle_tolerance_degrees: float = 2.0,
    rho_tolerance_px: float = 4.0,
) -> list[dict[str, float]]:
    carriers: list[dict[str, float]] = []
    angle_tol = np.deg2rad(angle_tolerance_degrees)
    for edge, assignment in zip(edges, assignments):
        if int(assignment) == 2:
            continue
        p0 = vertices[int(edge[0])]
        p1 = vertices[int(edge[1])]
        length = float(np.linalg.norm(p1 - p0))
        if length <= 1e-6:
            continue
        theta, rho, t0, t1 = line_parameters(p0, p1)
        matched = False
        for carrier in carriers:
            if angle_delta(theta, carrier["theta"]) <= angle_tol and abs(rho - carrier["rho"]) <= rho_tolerance_px:
                carrier["t_min"] = min(carrier["t_min"], t0, t1)
                carrier["t_max"] = max(carrier["t_max"], t0, t1)
                carrier["length"] += length
                carrier["edges"] += 1
                matched = True
                break
        if not matched:
            carriers.append(
                {
                    "theta": theta,
                    "rho": rho,
                    "t_min": min(t0, t1),
                    "t_max": max(t0, t1),
                    "length": length,
                    "edges": 1,
                }
            )
    return carriers


def match_carriers(
    gt_carriers: list[dict[str, float]],
    pred_carriers: list[dict[str, float]],
    *,
    angle_tolerance_degrees: float = 4.0,
    rho_tolerance_px: float = 8.0,
    min_overlap_fraction: float = 0.45,
) -> tuple[int, int]:
    if not gt_carriers or not pred_carriers:
        return 0, 0
    angle_tol = np.deg2rad(angle_tolerance_degrees)
    pairs: list[tuple[int, int]] = []
    for gt_idx, gt in enumerate(gt_carriers):
        for pred_idx, pred in enumerate(pred_carriers):
            if angle_delta(gt["theta"], pred["theta"]) > angle_tol:
                continue
            if abs(gt["rho"] - pred["rho"]) > rho_tolerance_px:
                continue
            overlap = max(0.0, min(gt["t_max"], pred["t_max"]) - max(gt["t_min"], pred["t_min"]))
            gt_span = max(gt["t_max"] - gt["t_min"], 1e-6)
            if overlap / gt_span >= min_overlap_fraction:
                pairs.append((gt_idx, pred_idx))
    matched_gt: set[int] = set()
    matched_pred: set[int] = set()
    for gt_idx, pred_idx in sorted(pairs):
        if gt_idx in matched_gt or pred_idx in matched_pred:
            continue
        matched_gt.add(gt_idx)
        matched_pred.add(pred_idx)
    return len(matched_gt), len(matched_pred)


def artifact_false_positive_rate(
    result: PlanarGraphResult,
    *,
    gt_vertices: np.ndarray,
    gt_edges: np.ndarray,
    artifact_mask: np.ndarray | None,
    image_size: int,
    enabled: bool,
) -> dict[str, float | int | None]:
    if not enabled or artifact_mask is None:
        return {
            "artifact_false_positive_rate": None,
            "artifact_overlap_edges": 0,
            "artifact_non_border_edges": 0,
        }
    matched_pred_edges = matched_pred_edge_indices(
        result,
        gt_vertices=gt_vertices,
        gt_edges=gt_edges,
        image_size=image_size,
    )
    non_border_edges = [
        (edge_idx, edge, int(assignment))
        for edge_idx, (edge, assignment) in enumerate(zip(result.edges_vertices, result.edges_assignment))
        if int(assignment) != 2
    ]
    if not non_border_edges:
        return {
            "artifact_false_positive_rate": 0.0,
            "artifact_overlap_edges": 0,
            "artifact_non_border_edges": 0,
        }
    overlap_edges = 0
    for edge_idx, edge, _ in non_border_edges:
        if edge_idx in matched_pred_edges:
            continue
        edge_mask = np.zeros((image_size, image_size), dtype=np.uint8)
        p0 = result.pixel_vertices[int(edge[0])]
        p1 = result.pixel_vertices[int(edge[1])]
        cv2.line(edge_mask, point(p0), point(p1), 255, max(2, image_size // 256), cv2.LINE_AA)
        if np.any((edge_mask > 0) & artifact_mask):
            overlap_edges += 1
    return {
        "artifact_false_positive_rate": ratio(overlap_edges, len(non_border_edges)),
        "artifact_overlap_edges": overlap_edges,
        "artifact_non_border_edges": len(non_border_edges),
    }


def matched_pred_edge_indices(
    result: PlanarGraphResult,
    *,
    gt_vertices: np.ndarray,
    gt_edges: np.ndarray,
    image_size: int,
) -> set[int]:
    _, pred_to_gt, _ = match_vertices(
        gt_vertices,
        result.pixel_vertices,
        tolerance=max(3.0, 5.0 * image_size / 768),
    )
    gt_lookup = {
        (min(int(v1), int(v2)), max(int(v1), int(v2)))
        for v1, v2 in np.asarray(gt_edges, dtype=np.int64)
    }
    matched: set[int] = set()
    for pred_idx, (pred_v1, pred_v2) in enumerate(result.edges_vertices):
        if int(pred_v1) not in pred_to_gt or int(pred_v2) not in pred_to_gt:
            continue
        key = (
            min(pred_to_gt[int(pred_v1)], pred_to_gt[int(pred_v2)]),
            max(pred_to_gt[int(pred_v1)], pred_to_gt[int(pred_v2)]),
        )
        if key in gt_lookup:
            matched.add(pred_idx)
    return matched


def run_model(model: torch.nn.Module, image: np.ndarray, device: torch.device) -> ModelPrediction:
    tensor = (
        torch.from_numpy(np.asarray(image, dtype=np.uint8).copy())
        .permute(2, 0, 1)
        .unsqueeze(0)
        .float()
        .to(device)
        / 255.0
    )
    with torch.no_grad():
        outputs = model(tensor)
    return ModelPrediction(
        line_prob=torch.sigmoid(outputs["line_logits"][0, 0]).detach().cpu().numpy().astype(np.float32),
        angle=outputs["angle"][0].detach().cpu().permute(1, 2, 0).numpy().astype(np.float32),
        junction_heatmap=torch.sigmoid(outputs["junction_logits"][0, 0]).detach().cpu().numpy().astype(np.float32),
        assignment_logits=outputs["assignment_logits"][0].detach().cpu(),
    )


def mask_artifact_evidence(
    prediction: ModelPrediction,
    artifact_mask: np.ndarray,
    *,
    dilation_px: int = 2,
) -> ModelPrediction:
    mask = artifact_mask.astype(np.uint8)
    if dilation_px > 0:
        kernel = np.ones((2 * dilation_px + 1, 2 * dilation_px + 1), dtype=np.uint8)
        mask = cv2.dilate(mask, kernel, iterations=1)
    keep = mask == 0
    return replace(
        prediction,
        line_prob=np.where(keep, prediction.line_prob, 0.0).astype(np.float32),
        junction_heatmap=np.where(keep, prediction.junction_heatmap, 0.0).astype(np.float32),
    )


def apply_oracle_border_chain(
    graph: AttributedPlanarGraph,
    *,
    target: TargetGraph,
    image_size: int,
) -> AttributedPlanarGraph:
    border_indices = np.flatnonzero(target.assignments == 2)
    if len(border_indices) == 0:
        return graph

    pixel_vertices = np.asarray(graph.pixel_vertices, dtype=np.float32).copy()
    vertex_support = np.asarray(graph.vertex_support, dtype=np.float32).copy()
    snap_tolerance = max(4.0, 8.0 * image_size / 768)
    gt_to_pred: dict[int, int] = {}
    used_pred_vertices: set[int] = set()
    border_gt_vertices = sorted(
        set(int(v) for edge_idx in border_indices for v in target.edges[int(edge_idx)])
    )
    for gt_idx in border_gt_vertices:
        gt_point = target.vertices[gt_idx]
        distances = np.linalg.norm(pixel_vertices - gt_point[None, :], axis=1)
        candidates = [
            int(idx)
            for idx in np.argsort(distances)
            if float(distances[int(idx)]) <= snap_tolerance and int(idx) not in used_pred_vertices
        ]
        if candidates:
            pred_idx = candidates[0]
            pixel_vertices[pred_idx] = gt_point
            gt_to_pred[gt_idx] = pred_idx
            used_pred_vertices.add(pred_idx)
            continue
        gt_to_pred[gt_idx] = int(len(pixel_vertices))
        pixel_vertices = np.vstack([pixel_vertices, gt_point])
        vertex_support = np.concatenate([vertex_support, np.array([1.0], dtype=np.float32)])
        used_pred_vertices.add(gt_to_pred[gt_idx])

    endpoint_map = {idx: idx for idx in range(len(pixel_vertices))}
    border_points = np.asarray([target.vertices[idx] for idx in border_gt_vertices], dtype=np.float32)
    for vertex_idx, vertex in enumerate(pixel_vertices):
        distances = np.linalg.norm(border_points - vertex[None, :], axis=1)
        nearest = int(np.argmin(distances))
        if float(distances[nearest]) <= snap_tolerance:
            endpoint_map[vertex_idx] = gt_to_pred[border_gt_vertices[nearest]]

    oracle_edges: list[tuple[int, int]] = []
    for edge_idx in border_indices:
        gt_v1, gt_v2 = (int(value) for value in target.edges[int(edge_idx)])
        pred_v1 = gt_to_pred[gt_v1]
        pred_v2 = gt_to_pred[gt_v2]
        if pred_v1 == pred_v2:
            continue
        oracle_edges.append((pred_v1, pred_v2))

    edge_records: dict[tuple[int, int], dict[str, Any]] = {}

    def add_record(
        v1: int,
        v2: int,
        *,
        assignment: int,
        support: float,
        confidence: float,
        margin: float,
        source: str,
        probabilities: np.ndarray | None,
    ) -> None:
        if v1 == v2:
            return
        key = (min(int(v1), int(v2)), max(int(v1), int(v2)))
        record = {
            "edge": key,
            "assignment": int(assignment),
            "support": float(support),
            "confidence": float(confidence),
            "margin": float(margin),
            "source": source,
            "probabilities": probabilities,
        }
        previous = edge_records.get(key)
        if previous is None:
            edge_records[key] = record
            return
        previous_is_oracle = previous["source"] == "oracle"
        record_is_oracle = source == "oracle"
        if record_is_oracle and not previous_is_oracle:
            edge_records[key] = record
        elif record_is_oracle == previous_is_oracle and support > float(previous["support"]):
            edge_records[key] = record

    oracle_prob = np.zeros(4, dtype=np.float32)
    oracle_prob[2] = 1.0
    for v1, v2 in oracle_edges:
        add_record(
            v1,
            v2,
            assignment=2,
            support=1.0,
            confidence=1.0,
            margin=1.0,
            source="oracle",
            probabilities=oracle_prob,
        )

    keep = np.flatnonzero(np.asarray(graph.edges_assignment) != 2)
    for edge_idx in keep:
        old_v1, old_v2 = (int(value) for value in graph.edges_vertices[int(edge_idx)])
        probabilities = None
        if graph.assignment_probabilities is not None:
            probabilities = np.asarray(graph.assignment_probabilities[int(edge_idx)], dtype=np.float32)
        add_record(
            endpoint_map.get(old_v1, old_v1),
            endpoint_map.get(old_v2, old_v2),
            assignment=int(graph.edges_assignment[int(edge_idx)]),
            support=float(graph.edge_support[int(edge_idx)]),
            confidence=float(graph.assignment_confidence[int(edge_idx)]),
            margin=float(graph.assignment_margin[int(edge_idx)]),
            source=graph.assignment_source[int(edge_idx)],
            probabilities=probabilities,
        )

    records = list(edge_records.values())
    edges_vertices = np.asarray([record["edge"] for record in records], dtype=np.int64)
    edges_assignment = np.asarray([record["assignment"] for record in records], dtype=np.int8)
    edge_support = np.asarray([record["support"] for record in records], dtype=np.float32)
    confidence = np.asarray([record["confidence"] for record in records], dtype=np.float32)
    margin = np.asarray([record["margin"] for record in records], dtype=np.float32)
    source = [str(record["source"]) for record in records]
    probabilities = None
    if any(record["probabilities"] is not None for record in records):
        probabilities = np.asarray(
            [
                record["probabilities"]
                if record["probabilities"] is not None
                else one_hot_assignment(int(record["assignment"]))
                for record in records
            ],
            dtype=np.float32,
        )
    pixel_vertices, vertex_support, edges_vertices = compact_vertices(
        pixel_vertices,
        vertex_support,
        edges_vertices,
    )
    return attributed_graph_from_arrays(
        pixel_vertices=pixel_vertices,
        edges_vertices=edges_vertices,
        edges_assignment=edges_assignment,
        edge_support=edge_support,
        vertex_support=vertex_support,
        assignment_confidence=confidence,
        assignment_margin=margin,
        assignment_source=source,
        assignment_probabilities=probabilities,
        image_size=image_size,
        debug={**graph.debug, "oracle_border_chain": True},
    )


def apply_oracle_assignments(
    graph: AttributedPlanarGraph,
    *,
    target: TargetGraph,
    image_size: int,
) -> AttributedPlanarGraph:
    _, pred_to_gt, _ = match_vertices(
        target.vertices,
        graph.pixel_vertices,
        tolerance=max(3.0, 5.0 * image_size / 768),
    )
    gt_lookup = {
        (min(int(v1), int(v2)), max(int(v1), int(v2))): int(idx)
        for idx, (v1, v2) in enumerate(target.edges)
    }
    assignments = np.full(graph.num_edges, 3, dtype=np.int8)
    confidence = np.zeros(graph.num_edges, dtype=np.float32)
    margin = np.zeros(graph.num_edges, dtype=np.float32)
    source: list[str] = []
    probabilities = np.zeros((graph.num_edges, 4), dtype=np.float32)
    for edge_idx, (v1, v2) in enumerate(graph.edges_vertices):
        if int(v1) not in pred_to_gt or int(v2) not in pred_to_gt:
            probabilities[edge_idx, 3] = 1.0
            source.append("unknown")
            continue
        gt_key = (
            min(pred_to_gt[int(v1)], pred_to_gt[int(v2)]),
            max(pred_to_gt[int(v1)], pred_to_gt[int(v2)]),
        )
        gt_idx = gt_lookup.get(gt_key)
        if gt_idx is None:
            probabilities[edge_idx, 3] = 1.0
            source.append("unknown")
            continue
        assignment = int(target.assignments[gt_idx])
        assignments[edge_idx] = assignment
        confidence[edge_idx] = 1.0
        margin[edge_idx] = 1.0
        probabilities[edge_idx, assignment] = 1.0
        source.append("oracle")
    return attributed_graph_from_arrays(
        pixel_vertices=graph.pixel_vertices,
        edges_vertices=graph.edges_vertices,
        edges_assignment=assignments,
        edge_support=graph.edge_support,
        vertex_support=graph.vertex_support,
        assignment_confidence=confidence,
        assignment_margin=margin,
        assignment_source=source,
        assignment_probabilities=probabilities,
        image_size=image_size,
        debug={**graph.debug, "oracle_assignments": True},
    )


