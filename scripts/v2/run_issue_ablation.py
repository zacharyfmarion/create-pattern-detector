def load_target_graph(
    root: Path,
    example: dict[str, Any],
    source_image_size: int,
    image_size: int,
) -> TargetGraph:
    sidecar = json.loads((root / example["sidecar"]).read_text(encoding="utf-8"))
    vertices = np.asarray(sidecar["vertices"], dtype=np.float32)
    if source_image_size != image_size:
        vertices = vertices * ((image_size - 1) / max(float(source_image_size - 1), 1.0))
    return TargetGraph(
        vertices=vertices.astype(np.float32),
        edges=np.asarray(sidecar["edges"], dtype=np.int64),
        assignments=np.asarray(sidecar["assignments"], dtype=np.int8),
        image_size=image_size,
    )


def load_rgb(path: Path, *, image_size: int) -> np.ndarray:
    image = Image.open(path).convert("RGB")
    if image.size != (image_size, image_size):
        image = image.resize((image_size, image_size), Image.Resampling.BILINEAR)
    return np.asarray(image, dtype=np.uint8)


def load_artifact_mask(
    root: Path,
    example: dict[str, Any],
    *,
    issue: str,
    image_size: int,
) -> np.ndarray | None:
    if issue not in ARTIFACT_ISSUES:
        return None
    mask_rel = example.get("oracle_mask")
    if not mask_rel:
        return None
    image = Image.open(root / mask_rel).convert("RGB")
    if image.size != (image_size, image_size):
        image = image.resize((image_size, image_size), Image.Resampling.NEAREST)
    array = np.asarray(image, dtype=np.uint8)
    return np.any(array > 0, axis=2)


def target_line_mask(target: TargetGraph) -> np.ndarray:
    mask = np.zeros((target.image_size, target.image_size), dtype=np.uint8)
    width = max(5, int(round(6 * target.image_size / 1024)))
    for edge in target.edges:
        p0 = target.vertices[int(edge[0])]
        p1 = target.vertices[int(edge[1])]
        cv2.line(mask, point(p0), point(p1), 255, width, cv2.LINE_AA)
    return mask > 0


def write_graph_overlay(
    path: Path,
    image: np.ndarray,
    graph: AttributedPlanarGraph,
    *,
    label: str,
) -> None:
    canvas = Image.fromarray(image.copy())
    draw = ImageDraw.Draw(canvas)
    vertices = graph.pixel_vertices
    for edge_idx, edge in enumerate(graph.edges_vertices):
        assignment = int(graph.edges_assignment[edge_idx])
        color = ASSIGNMENT_COLORS.get(assignment, ASSIGNMENT_COLORS[3])
        width = 4 if assignment == 2 else 2
        p0 = tuple(float(value) for value in vertices[int(edge[0])])
        p1 = tuple(float(value) for value in vertices[int(edge[1])])
        draw.line([p0, p1], fill=color, width=width)
    for x, y in vertices:
        draw.ellipse((float(x) - 2, float(y) - 2, float(x) + 2, float(y) + 2), fill=(28, 160, 80))
    draw.rectangle((0, 0, canvas.width, 24), fill=(255, 255, 255))
    draw.text((6, 6), label, fill=(20, 20, 20), font=ImageFont.load_default())
    path.parent.mkdir(parents=True, exist_ok=True)
    canvas.save(path)


def write_contact_sheet(items: list[dict[str, Any]], path: Path, *, cell_width: int = 260) -> None:
    if not items:
        return
    columns = [
        ("issue_image", "Issue"),
        ("issue_baseline_model", "Baseline"),
        ("artifact_mask_oracle", "Artifact Oracle"),
        ("oracle_border_chain", "Border Oracle"),
        ("oracle_vector_evidence", "Evidence Oracle"),
        ("target", "Target"),
    ]
    gap = 12
    label_height = 58
    footer_height = 20
    cell_height = cell_width + label_height + footer_height
    sheet_width = gap + len(columns) * (cell_width + gap)
    sheet_height = gap + len(items) * (cell_height + gap)
    sheet = Image.new("RGB", (sheet_width, sheet_height), "white")
    draw = ImageDraw.Draw(sheet)
    font = ImageFont.load_default()
    for row_idx, item in enumerate(items):
        y = gap + row_idx * (cell_height + gap)
        draw.text((gap, y), f"{item['issue']} / {item['id']}", fill=(20, 20, 20), font=font)
        for col_idx, (key, label) in enumerate(columns):
            x = gap + col_idx * (cell_width + gap)
            if item.get(key) is None:
                cell = Image.new("RGB", (cell_width, cell_width), (248, 248, 248))
                ImageDraw.Draw(cell).text((10, 10), "n/a", fill=(120, 120, 120), font=font)
            else:
                cell = Image.open(item[key]).convert("RGB")
            cell.thumbnail((cell_width, cell_width), Image.Resampling.LANCZOS)
            top = y + label_height
            sheet.paste(cell, (x, top))
            draw.text((x, top + cell.height + 3), label, fill=(80, 80, 80), font=font)
    path.parent.mkdir(parents=True, exist_ok=True)
    sheet.save(path)


def line_parameters(p0: np.ndarray, p1: np.ndarray) -> tuple[float, float, float, float]:
    direction = np.asarray(p1, dtype=np.float64) - np.asarray(p0, dtype=np.float64)
    theta = float(np.arctan2(direction[1], direction[0]) % np.pi)
    unit = np.array([np.cos(theta), np.sin(theta)], dtype=np.float64)
    normal = np.array([-np.sin(theta), np.cos(theta)], dtype=np.float64)
    rho = float(np.dot(normal, p0))
    if rho < 0:
        rho = -rho
        theta = float((theta + np.pi) % np.pi)
        unit = -unit
    t0 = float(np.dot(unit, p0))
    t1 = float(np.dot(unit, p1))
    return theta, rho, t0, t1


def angle_delta(a: float, b: float) -> float:
    delta = abs((a - b) % np.pi)
    return float(min(delta, np.pi - delta))


def point(point_array: np.ndarray) -> tuple[int, int]:
    return (int(round(float(point_array[0]))), int(round(float(point_array[1]))))


def ratio(numerator: int | float, denominator: int | float) -> float:
    return float(numerator / denominator) if denominator else 0.0


def f1(precision: float, recall: float) -> float:
    return 2.0 * precision * recall / (precision + recall) if precision + recall else 0.0


def mean_non_null(values: Iterable[Any]) -> float | None:
    filtered = [float(value) for value in values if value is not None]
    if not filtered:
        return None
    return float(np.mean(filtered))


def nullable_delta(value: Any, baseline: Any) -> float | None:
    if value is None or baseline is None:
        return None
    return float(value) - float(baseline)


def format_metric(value: Any) -> str:
    if value is None:
        return ""
    return f"{float(value):.4f}"


def resolve_path(path: Path) -> Path:
    return path if path.is_absolute() else REPO_ROOT / path


def json_safe(value: Any) -> Any:
    if isinstance(value, Path):
        return value.as_posix()
    if isinstance(value, np.ndarray):
        return [json_safe(item) for item in value.tolist()]
    if isinstance(value, np.generic):
        return value.item()
    if isinstance(value, dict):
        return {str(key): json_safe(item) for key, item in value.items()}
    if isinstance(value, (list, tuple)):
        return [json_safe(item) for item in value]
    return value


